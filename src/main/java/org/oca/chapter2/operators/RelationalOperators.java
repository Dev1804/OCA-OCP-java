package org.oca.chapter2.operators;

public class RelationalOperators {
    // Comparing greater (> >=) and lesser values (<, <=)
    // Comparing values for equality (==) and inequality(!=)

    // the former can be used with all number data types
    // the former can be used with all primitive types

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a == b);
        System.out.println(a != b);
        boolean b1 = false;
        System.out.println(b1 == true);
        System.out.println(b1 != true);
        System.out.println(b1 == false);
        System.out.println(b1 != false );
    }
}
