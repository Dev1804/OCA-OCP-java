package org.oca.chapter2.operators;

public class ComparingPrimitivesUsingAssignmentOp {

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a = b);
        boolean b1 = false;
        System.out.println(b1 = true);
        System.out.println(b1 = false);
    }
}
